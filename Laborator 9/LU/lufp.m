function [L1, U] = lufp(A)
%=========================================================================
%Functie de determinare LU.
%Copyright (C) Andreas Mihalea
%=========================================================================
% INPUT:  A = 1 6 7      OUTPUT: U = 1  6  7   L = 1  0 0
%             2 8 5                  0 -4 -9       2  1 0
%             9 6 8                  0  0 53       9 12 1
%=========================================================================

n = size(A, 1);
L = eye(n); 
for k = 1:n
    L(k+1:n,k) = A(k+1:n,k) / A(k,k);
    for l=k+1:n
        A(l,:) = A(l,:)-L(l,k)*A(k,:);
    end
end
U = A;
L1=L;
end
